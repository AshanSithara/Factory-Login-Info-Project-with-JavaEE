package com.proje.dto;

public class LoginControlDatesDto {

    private String controlDate;
    private String loginHour;
    private String outHour;

    public String getControlDate() {
        return controlDate;
    }

    public void setControlDate(String controlDate) {
        this.controlDate = controlDate;
    }

    public String getLoginHour() {
        return loginHour;
    }

    public void setLoginHour(String loginHour) {
        this.loginHour = loginHour;
    }

    public String getOutHour() {
        return outHour;
    }

    public void setOutHour(String outHour) {
        this.outHour = outHour;
    }
}
